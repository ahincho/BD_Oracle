    private void actualizar() {
        String codigo = PerCod.getText();
        String nombre = PerNom.getText();
        String cargo = PerCarCod.getText();
        String fecIngDia = PerFecIngDia.getText();
        String fecIngMes = PerFecIngMes.getText();
        String fecIngAnio = PerFecIngAnio.getText();
        String nivExp = PerNivExp.getText();
        String estOcuCod = PerEstOcuCod.getText();
        String estado = PerEstRegPer.getText();
        switch (flagBoton) {
            case 1:
                //AgregarBoton.setEnabled(false);
                if (((codigo.equals("")) || (nombre.equals("")))) {
                    JOptionPane.showMessageDialog(null, "Falta Ingresar Datos!");
                } else {
                    if ((carFlaAct == 1)) {
                        try {
                            String sql = "insert into " + NOMBRE_TABLA + " (" + NOMBRE_DATO_1 + ", " + NOMBRE_DATO_2 + ", " + NOMBRE_DATO_3 + ", " + NOMBRE_DATO_4 
                                    + ", " + NOMBRE_DATO_5 + ", " + NOMBRE_DATO_6 + ", " + NOMBRE_DATO_7 + ", " + NOMBRE_DATO_8 + ", " + NOMBRE_DATO_9 + ") "
                                    + "values('" + codigo + "','" + nombre + "','" + cargo + "','" + fecIngAnio + "','" + fecIngMes + "','" + fecIngDia
                                    + "','" + nivExp + "','" + estOcuCod + "','" + estado + "')";
                            conet = con1.getConnection();
                            st = conet.createStatement();
                            st.executeUpdate(sql);
                            JOptionPane.showMessageDialog(null, "Nuevo " + NOMBRE_INSTACIA + " Registrado!");
                            limpiarFormulario();
                            desactivarFormulario();
                            limpiarTabla();
                        } catch (Exception e) {
                            System.out.println("Error SQL: " + e);
                        }
                    } else {
                        System.out.println("Flag Error");
                    }
                }
                break;
            case 2:
                //ModificarBoton.setEnabled(false);
                if (((nombre.equals("")))) {
                    JOptionPane.showMessageDialog(null, "Falta Ingresar Nombre!");
                } else {
                    if ((carFlaAct == 1)) {
                        try {
                            String sql = "update " + NOMBRE_TABLA + " set " + NOMBRE_DATO_2 + "='" + nombre + "', " + NOMBRE_DATO_3 + "='" + cargo + "', "
                                       + NOMBRE_DATO_4 + "='" + fecIngAnio + "', " + NOMBRE_DATO_5 + "='" + fecIngMes + "', " + NOMBRE_DATO_6 + "='" + fecIngDia
                                       + "', " + NOMBRE_DATO_7 + "='" + nivExp + "', " + NOMBRE_DATO_8 + "='" + estOcuCod + "' where " + NOMBRE_DATO_1 + "=" + codigo;
                            conet = con1.getConnection();
                            st = conet.createStatement();
                            st.executeUpdate(sql);
                            JOptionPane.showMessageDialog(null, NOMBRE_INSTACIA + " Actualizado!");
                            limpiarFormulario();
                            desactivarFormulario();
                            limpiarTabla();
                        } catch (Exception e) {
                            System.out.println("Error SQL: " + e);
                        }
                    } else {
                        System.out.println("Flag Error");
                    }
                }
                break;
            case 3:
                //EliminarBoton.setEnabled(false);
                if (carFlaAct == 1) {
                    try {
                        String sql = "update " + NOMBRE_TABLA + " set " + NOMBRE_DATO_9 + "='*' where " + NOMBRE_DATO_1 + "=" + codigo;
                        conet = con1.getConnection();
                        st = conet.createStatement();
                        st.executeUpdate(sql);
                        JOptionPane.showMessageDialog(null, NOMBRE_INSTACIA + " Eliminado!");
                        limpiarFormulario();
                        desactivarFormulario();
                        limpiarTabla();
                    } catch (Exception e) {
                        System.out.println("Error SQL: " + e);
                    }
                }
                break;
            case 4:
                //InactivarBoton.setEnabled(false);
                if (carFlaAct == 1) {
                    try {
                        String sql = "update " + NOMBRE_TABLA + " set " + NOMBRE_DATO_9 + "='I' where " + NOMBRE_DATO_1 + "=" + codigo;
                        conet = con1.getConnection();
                        st = conet.createStatement();
                        st.executeUpdate(sql);
                        JOptionPane.showMessageDialog(null, NOMBRE_INSTACIA + " Inactivado!");
                        limpiarFormulario();
                        desactivarFormulario();
                        limpiarTabla();
                    } catch (Exception e) {
                        System.out.println("Error SQL: " + e);
                    }
                }
                break;
            case 5:
                //ReactivarBoton.setEnabled(false);
                if (estado.equals("*") || estado.equals("I")) {
                    if (carFlaAct == 1) {
                        try {
                            String sql = "update " + NOMBRE_TABLA + " set " + NOMBRE_DATO_9 + "='A' where " + NOMBRE_DATO_1 + "=" + codigo;
                            conet = con1.getConnection();
                            st = conet.createStatement();
                            st.executeUpdate(sql);
                            JOptionPane.showMessageDialog(null, NOMBRE_INSTACIA + " Reactivado!");
                            limpiarFormulario();
                            desactivarFormulario();
                            limpiarTabla();
                        } catch (Exception e) {
                            System.out.println("Error SQL: " + e);
                        }
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "El estado se encuentra activo");
                }
                break;
            default:
                JOptionPane.showMessageDialog(null, "No se realizo ninguna modificacion");
                break;
        }
        flagBoton = 0;

    }

