    private void consulta() {
        String sql = "select * from "  +NOMBRE_TABLA + " order by " + NOMBRE_DATO_1;

        try {
            conet = con1.getConnection();
            st = conet.createStatement();
            rs = st.executeQuery(sql);
            Object[] descClientes = new Object[CANTIDAD_DATOS];
            modelo = (DefaultTableModel) Tabla.getModel();
            while (rs.next()) {
                descClientes[0] = rs.getInt(NOMBRE_DATO_1);
                descClientes[1] = rs.getString(NOMBRE_DATO_2);
                descClientes[2] = rs.getInt(NOMBRE_DATO_3);
                descClientes[3] = rs.getInt(NOMBRE_DATO_4);
                descClientes[4] = rs.getInt(NOMBRE_DATO_5);
                descClientes[5] = rs.getInt(NOMBRE_DATO_6);
                descClientes[6] = rs.getInt(NOMBRE_DATO_7);
                descClientes[7] = rs.getInt(NOMBRE_DATO_8);
                descClientes[8] = rs.getString(NOMBRE_DATO_9);
                modelo.addRow(descClientes);
            }
            Tabla.setModel(modelo);

        } catch (Exception e) {
        }
    }

